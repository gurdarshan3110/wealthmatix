    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $title = 'Add New Agent';
        return view('agent.create', compact('title'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        $validatedData = $request->validate([
            'name' => 'required',
            'email' => 'required|email|unique:users',
            'phone_no' => ['required', 'regex:/^(\+\d{1,3}[- ]?)?\d{10,}$/', 'unique:agencies'],
            'status' => 'required'
        ]);

        $autoGeneratedPassword = '12345678';
        $agentCode = $this->generateRandomString($request->name);
        $input['agent_code'] = $agentCode;
        $input['agent_id'] = Auth::user()->agentId();
        $agent = Agent::create($input);
        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'username' => $request->email,
            'phone_no' => $request->phone_no,
            'password' => Hash::make($autoGeneratedPassword),
            'user_type' => User::USER_AGENT,
        ]);
        $user->agents()->attach($agent);

        return redirect()->route('agents.index', $agent->id)->with('success', 'Agent created successfully.');
    }

